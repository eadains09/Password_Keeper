
public class PassEntry {
	String name;
	String username;
	String URL;
	String password;
		
	PassEntry(String[] cc)
	{
		name = cc[0];
		username = cc[1];
		password = cc[2];
		URL = cc[3];
	}
		
	public String toString()
	{
		String curr=null;
		curr= name + " " + username + " " + password + " " + URL;
		return curr;
	}
}
